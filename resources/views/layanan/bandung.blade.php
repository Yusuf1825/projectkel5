    <p>🏙️ Sejarah Kota Bandung
Bandung bermula dari sebuah pemukiman kecil di dataran tinggi Priangan. Nama "Bandung" berasal dari kata "bendung" atau "banding", yang mengacu pada terbendungnya Sungai Citarum oleh lava Gunung Tangkuban Perahu, membentuk danau purba.

Pada masa penjajahan Belanda, Bandung dijuluki "Parijs van Java" karena tata kotanya yang indah dan gaya hidup masyarakat Eropanya. Kota ini menjadi pusat pendidikan, perdagangan, dan militer di Jawa Barat.

Pada tahun 1955, Bandung menjadi tuan rumah Konferensi Asia Afrika, sebuah momen bersejarah yang menjadikan kota ini dikenal di dunia internasional. Kini, Bandung menjadi pusat industri kreatif, pendidikan, dan pariwisata.

