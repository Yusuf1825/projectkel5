Jakarta awalnya adalah pelabuhan kecil bernama Sunda Kelapa, bagian dari Kerajaan Sunda. Pada tahun 1527, Fatahillah menaklukkan wilayah ini dan mengganti namanya menjadi Jayakarta, yang berarti "kemenangan yang sempurna".

